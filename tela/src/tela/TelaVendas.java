    private void finalizarVendaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarVendaButtonActionPerformed
        
        SimpleDateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
        Venda vend = new Venda();
        vend.setItens(itensVendas);
        vend.Cliente(clientes);
        if (jRadioButton1.isSelected()) {
            vend.setFormaPagamento(jRadioButton1.getText());
        }
        if (jRadioButton2.isSelected()) {
            vend.setFormaPagamento(jRadioButton2.getText());
        }
        vend.setDataVenda(x);
        vend.setTotalVenda();
        try {
            ServicoVenda.cadastrarVenda(vend);
        } catch (VendaException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            return;
        } catch (DataSourceException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            return;
        }
        JOptionPane.showMessageDialog(this, "Venda finalizada com sucesso");
        DefaultTableModel tabela = (DefaultTableModel) ProdTable.getModel();
        tabela.setRowCount(0);
        precoT=0;
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        totalVendaText.setText("");
        cpfText.setText("");
        nomeText.setText("");
        itensVendas = new ArrayList<ItensVenda>();
        clientes = new Cliente();
        //Finalizado
    }//GEN-LAST:event_finalizarVendaButtonActionPerformed

