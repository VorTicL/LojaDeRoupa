    private void buscarProdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarProdButtonActionPerformed
        buscaProduto dlg = new buscaProduto(this, true);
        ItensVenda itens = dlg.selecionarItemVenda();
        boolean prodI=true;
        if (!itensVendas.isEmpty()) {
            for (int i = 0; i < itensVendas.size(); i++) {
                if (itensVendas.get(i).getProduto().getID()==itens.getProduto().getID()) {
                    prodI=false;
                    break;
                }
            }
        }    
        if (itens != null && itens.getProduto() != null && itens.getQtdProduto() > 0 && prodI==true) {
            Object[] populaTab = new Object[7];
            DefaultTableModel tabela = (DefaultTableModel) ProdTable.getModel();
            populaTab[0] = itens.getProduto().getNome();
            populaTab[1] = itens.getProduto().getTamanho();
            populaTab[2] = itens.getProduto().getCor();
            populaTab[3] = itens.getProduto().getColecao();
            populaTab[4] = itens.getProduto().getPreco();
            populaTab[5] = itens.getProduto().getQuantidade();
            populaTab[6] = itens.getQtdProduto();
            tabela.addRow(populaTab);
            precoT += (itens.getProduto().getPreco()) * ((double)itens.getQtdProduto());
            itensVendas.add(itens);
            totalVendaText.setText(Double.toString(precoT));
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nenhum Produto Selecionado ou produto já esta no carrinho");
        }
        //Finalizado
    }//GEN-LAST:event_buscarProdButtonActionPerformed

